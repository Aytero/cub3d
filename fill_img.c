#include "cub3d.h"

void	fill_img(t_all *all)
{
	int		y;
	int		x;

	y = 0;
	while (y < all->res_y)
	{
		x = 0;
		while (x < all->res_x)
		{
			all->img.addr[all->img.line_len / 4 * y + x] = all->buf[y][x];
			x++;
		}
		y++;
	}
}
